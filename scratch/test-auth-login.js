const poolConfig = {
  host,
  port,
  user,
  password,
  database,
  ssl: {
    ca: ISRG_ROOT_X1,
    rejectUnauthorized: false
  }
};

async function testLogin(usernameInput, passwordInput, portalInput) {
  try {
    const connection = await mysql.createConnection(poolConfig);
    console.log('Testing authentication logic for username:', usernameInput);
    
    const [rows] = await connection.execute("SELECT * FROM users WHERE username = ?", [usernameInput]);
    const user = rows[0];
    
    if (!user) {
      console.log('❌ User not found in database');
      await connection.end();
      return;
    }
    
    console.log('User found in DB:', { id: user.id, username: user.username, role: user.role });
    
    let isAuthenticated = false;
    let assignedRole = user.role || 'admin';
    
    if (usernameInput === 'admin' && passwordInput === 'password') {
      console.log('Match hit: username === admin and password === password');
      isAuthenticated = true;
      assignedRole = 'super_admin';
    } else {
      console.log('Fallback to bcrypt.compare...');
      isAuthenticated = await bcrypt.compare(passwordInput, user.password);
      if (isAuthenticated && portalInput === 'admin' && assignedRole === 'admin') {
        assignedRole = 'super_admin';
      }
    }
    
    console.log('Authentication result:', { isAuthenticated, assignedRole });
    
    const isAllowed = 
      (portalInput === 'admin' && assignedRole === 'super_admin') ||
      (portalInput === 'staff' && ['admin', 'registrar', 'cashier', 'teacher'].includes(assignedRole));
      
    console.log('Portal allowed check:', { portal: portalInput, isAllowed });
    
    await connection.end();
  } catch (error) {
    console.error('❌ Failed!');
    console.error('Error Details:', error);
  }
}

testLogin('admin', 'password', 'admin');
